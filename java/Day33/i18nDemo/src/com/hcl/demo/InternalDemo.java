package com.hcl.demo;

import java.text.NumberFormat;
import java.util.Locale;

public class InternalDemo {
	public static void main(String[] args) {
		double d=101234.222;
		NumberFormat nf=NumberFormat.getInstance(Locale.ITALY);
		NumberFormat nf1=NumberFormat.getInstance(Locale.US);
		NumberFormat nf2=NumberFormat.getInstance(Locale.CHINA);
		NumberFormat nf4=NumberFormat.getInstance(Locale.GERMAN);
		System.out.println(d);
		System.out.println("italian "+ nf.format(d));
		System.out.println("us "+ nf1.format(d));
		System.out.println("china "+nf2.format(d));
		System.out.println("germany "+nf4.format(d));
		}
	}


